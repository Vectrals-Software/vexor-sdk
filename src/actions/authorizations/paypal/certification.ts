import https from 'https';

export async function downloadAndCacheCertificate(url: string): Promise<string> {
    // Implement caching logic here if needed
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            let data = '';
            res.on('data', (chunk) => {
                data += chunk;
            });
            res.on('end', () => {
                resolve(data);
            });
        }).on('error', (err) => {
            reject(err);
        });
    });
}
